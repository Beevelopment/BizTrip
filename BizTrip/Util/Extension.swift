        let gardientLayer = CAGradientLayer()
        gardientLayer.frame = bounds
        gardientLayer.colors = [colorOne.cgColor, colorTwo.cgColor]
        gardientLayer.locations = [0.0, 1.0]
        gardientLayer.startPoint = CGPoint(x: 1.0, y: 1.0)
        gardientLayer.endPoint = CGPoint(x: 0.0, y: 0.0)
        
        layer.insertSublayer(gardientLayer, at: 0)
    }
